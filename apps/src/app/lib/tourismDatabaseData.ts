export function normalizeTourismCountryKey(value: string | undefined | null) {
  if (!value) return "";
  return value
    .toLowerCase()
    .trim()
    .replace(/\s+/g, "_")
    .replace(/[^a-z0-9]+/g, "_")
    .replace(/^_+|_+$/g, "");
}

export function getTourismAttractions(countryName?: string | null, countryId?: number | null) {
  const direct = countryId != null ? tourismDatabaseById[countryId] : undefined;
  const slug = direct || normalizeTourismCountryKey(countryName);
  const data = tourismDatabaseBySlug[slug];
  if (!data?.tempat_wisata) {
    return [];
  }
  return data.tempat_wisata;
}

